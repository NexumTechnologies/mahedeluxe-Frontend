function getNestedValue(source: DashboardTree, path: string): string | undefined {
  const result = path.split(".").reduce<DashboardValue | undefined>((current, key) => {
    if (!current || typeof current === "string") return undefined;
    return current[key];
  }, source);

  return typeof result === "string" ? result : undefined;
}

export function translateDashboard(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const template =
    getNestedValue(dashboardDictionary[locale], key) ??
    getNestedValue(dashboardDictionary[DEFAULT_LOCALE], key) ??
    key;

  if (!vars) return template;

  return Object.entries(vars).reduce((output, [name, value]) => {
    return output.replaceAll(`{{${name}}}`, String(value));
  }, template);
}

export function formatDashboardDate(locale: Locale, value?: string | number | Date | null) {
  if (!value) return "-";
  return new Intl.DateTimeFormat(locale === "ar" ? "ar" : "en", {
    year: "numeric",
    month: "short",
    day: "numeric",
  }).format(new Date(value));
}

export function formatDashboardDateTime(
  locale: Locale,
  value?: string | number | Date | null,
) {
  if (!value) return "-";
  return new Intl.DateTimeFormat(locale === "ar" ? "ar" : "en", {
    year: "numeric",
    month: "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit",
  }).format(new Date(value));
}

export function getOrderStatusLabel(locale: Locale, status?: string | null) {
  const normalized = String(status || "pending").toLowerCase();
  if (normalized === "delivered") return translateDashboard(locale, "common.delivered");
  if (normalized === "processing") return translateDashboard(locale, "common.processing");
  if (normalized === "cancelled") return translateDashboard(locale, "common.cancelled");
  if (normalized === "completed") return translateDashboard(locale, "common.completed");
  return translateDashboard(locale, "common.pending");
}

export function getVerificationStatusLabel(locale: Locale, status?: string | null) {
  const normalized = String(status || "pending").toLowerCase();
  if (normalized === "approved") return translateDashboard(locale, "common.approved");
  if (normalized === "rejected") return translateDashboard(locale, "common.rejected");
  return translateDashboard(locale, "common.pending");
}
