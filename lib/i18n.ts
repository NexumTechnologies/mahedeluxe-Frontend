function getNestedValue(source: DictionaryTree, path: string): string | undefined {
  const result = path.split(".").reduce<DictionaryValue | undefined>((current, key) => {
    if (!current || typeof current === "string") return undefined;
    return current[key];
  }, source);

  return typeof result === "string" ? result : undefined;
}

export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const template =
    getNestedValue(dictionary[locale], key) ??
    getNestedValue(dictionary[DEFAULT_LOCALE], key) ??
    key;

  if (!vars) return template;

  return Object.entries(vars).reduce((output, [varName, value]) => {
    return output.replaceAll(`{{${varName}}}`, String(value));
  }, template);
}